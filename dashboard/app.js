/* Wanderland tree dashboard — D3 collapsible horizontal tree.
   Data: window.TREE (generated by build.js from wanderland.tree.md). */
'use strict';
(function () {
  const DATA = window.TREE;
  document.getElementById('title').textContent = DATA.name;
  document.title = DATA.name + ' — tree dashboard';

  const PALETTE = ['var(--c0)', 'var(--c1)', 'var(--c2)', 'var(--c3)', 'var(--c4)', 'var(--c5)'];
  const svg = d3.select('svg');
  const g = svg.append('g');
  const gLink = g.append('g');
  const gNode = g.append('g');

  const dx = 34, dy = 240;
  const tree = d3.tree().nodeSize([dx, dy]);
  const diagonal = d3.linkHorizontal().x(d => d.y).y(d => d.x);

  const root = d3.hierarchy(DATA);
  root.x0 = 0; root.y0 = 0;
  root.descendants().forEach((d, i) => {
    d.id = i;
    d._children = d.children;
    let a = d; while (a.depth > 1) a = a.parent;
    d.branch = a.depth === 0 ? -1 : a.parent.children.indexOf(a);
  });
  // start collapsed below depth 2
  root.descendants().forEach(d => { if (d.depth >= 2 && d.children) { d._children = d.children; d.children = null; } });

  const zoom = d3.zoom().scaleExtent([0.3, 2.5]).on('zoom', e => g.attr('transform', e.transform));
  svg.call(zoom);

  const colorOf = d => d.depth === 0 ? 'var(--c0)' : PALETTE[(d.branch >= 0 ? d.branch : 0) % PALETTE.length];
  const MAXW = 230;
  const rawW = s => s.length * 7.2 + 38;
  const nodeW = s => Math.min(rawW(s), MAXW);
  const maxChars = Math.floor((MAXW - 38) / 7.2);
  const fitText = s => s.length > maxChars ? s.slice(0, maxChars - 1) + '…' : s;
  const collapsed = d => !!(d._children && !d.children);

  function update(source) {
    const dur = 320;
    const nodes = root.descendants().reverse();
    const links = root.links();
    tree(root);

    const t = svg.transition().duration(dur);

    const node = gNode.selectAll('g.node').data(nodes, d => d.id);

    const nodeEnter = node.enter().append('g')
      .attr('class', 'node')
      .attr('transform', () => 'translate(' + source.y0 + ',' + source.x0 + ')')
      .attr('fill-opacity', 0).attr('stroke-opacity', 0)
      .on('click', (e, d) => { d.children = d.children ? null : d._children; update(d); });

    nodeEnter.append('rect')
      .attr('height', 28).attr('y', -14).attr('rx', 12).attr('ry', 12)
      .attr('x', d => -nodeW(d.data.name) / 2)
      .attr('width', d => nodeW(d.data.name))
      .attr('fill', 'var(--card)')
      .attr('stroke', colorOf).attr('stroke-width', 1.6);

    nodeEnter.append('circle').attr('r', 4)
      .attr('cx', d => -nodeW(d.data.name) / 2).attr('fill', colorOf);

    nodeEnter.append('text').attr('class', 'label')
      .attr('text-anchor', 'middle').attr('dy', '0.32em')
      .text(d => fitText(d.data.name))
      .append('title').text(d => d.data.name);

    const badge = nodeEnter.append('g').attr('class', 'badge')
      .attr('transform', d => 'translate(' + (nodeW(d.data.name) / 2 - 2) + ',0)');
    badge.append('circle').attr('r', 9).attr('fill', colorOf);
    badge.append('text').attr('class', 'count')
      .attr('text-anchor', 'middle').attr('dy', '0.32em');

    const merged = node.merge(nodeEnter);
    merged.select('.badge').style('display', d => collapsed(d) ? null : 'none');
    merged.select('.badge .count').text(d => collapsed(d) ? d._children.length : '');

    merged.transition(t)
      .attr('transform', d => 'translate(' + d.y + ',' + d.x + ')')
      .attr('fill-opacity', 1).attr('stroke-opacity', 1);

    node.exit().transition(t).remove()
      .attr('transform', () => 'translate(' + source.y + ',' + source.x + ')')
      .attr('fill-opacity', 0).attr('stroke-opacity', 0);

    const link = gLink.selectAll('path').data(links, d => d.target.id);
    const linkEnter = link.enter().append('path').attr('class', 'link')
      .attr('stroke', d => colorOf(d.target))
      .attr('d', () => { const o = { x: source.x0, y: source.y0 }; return diagonal({ source: o, target: o }); });
    link.merge(linkEnter).transition(t).attr('d', diagonal).attr('stroke', d => colorOf(d.target));
    link.exit().transition(t).remove()
      .attr('d', () => { const o = { x: source.x, y: source.y }; return diagonal({ source: o, target: o }); });

    root.eachBefore(d => { d.x0 = d.x; d.y0 = d.y; });
  }

  function fit() {
    tree(root);
    let minX = Infinity, maxX = -Infinity, minY = Infinity, maxY = -Infinity;
    root.each(d => { minX = Math.min(minX, d.x); maxX = Math.max(maxX, d.x); minY = Math.min(minY, d.y); maxY = Math.max(maxY, d.y); });
    const w = window.innerWidth, h = window.innerHeight;
    const tw = (maxY - minY) + 360, th = (maxX - minX) + 140;
    const k = Math.min(1.4, Math.min(w / tw, h / th));
    const cx = (minY + maxY) / 2, cy = (minX + maxX) / 2;
    svg.transition().duration(420).call(zoom.transform,
      d3.zoomIdentity.translate(w / 2 - k * cx, h / 2 - k * cy).scale(k));
  }

  d3.select('#expand').on('click', () => { root.each(d => { if (d._children) d.children = d._children; }); update(root); setTimeout(fit, 340); });
  d3.select('#collapse').on('click', () => { root.descendants().forEach(d => { if (d.depth >= 1 && d.children) { d._children = d.children; d.children = null; } }); update(root); setTimeout(fit, 340); });
  d3.select('#fit').on('click', fit);
  window.addEventListener('resize', fit);

  update(root);
  setTimeout(fit, 60);
})();
