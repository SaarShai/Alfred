/* Pursuits dashboard — multiple D3 collapsible trees on one zoomable canvas.
   Data: window.TREES (array of roots, generated by build.js from pursuits.md). */
'use strict';
(function () {
  const TREES = window.TREES;
  const PALETTE = ['var(--c0)', 'var(--c1)', 'var(--c2)', 'var(--c3)', 'var(--c4)', 'var(--c5)'];
  const TREE_COLORS = ['var(--c0)', 'var(--c2)', 'var(--c3)', 'var(--c5)']; // one base hue per pursuit

  const dx = 34, dy = 240, GAP = 80, MARGIN_L = 60;
  const tree = d3.tree().nodeSize([dx, dy]);
  const diagonal = d3.linkHorizontal().x(d => d.y).y(d => d.x);

  const svg = d3.select('svg');
  const canvas = svg.append('g');
  const zoom = d3.zoom().scaleExtent([0.25, 2.5]).on('zoom', e => canvas.attr('transform', e.transform));
  svg.call(zoom);

  const MAXW = 230;
  const rawW = s => s.length * 7.2 + 38;
  const nodeW = (s, root) => root ? rawW(s) : Math.min(rawW(s), MAXW);
  const maxChars = Math.floor((MAXW - 38) / 7.2);
  const fitText = (s, root) => (!root && s.length > maxChars) ? s.slice(0, maxChars - 1) + '…' : s;
  const collapsed = d => !!(d._children && !d.children);
  const colorOf = d => d.depth === 0
    ? TREE_COLORS[d.ti % TREE_COLORS.length]
    : PALETTE[(d.branch >= 0 ? d.branch : 0) % PALETTE.length];

  let uid = 0;
  const states = TREES.map((data, ti) => {
    const root = d3.hierarchy(data);
    root.x0 = 0; root.y0 = 0;
    root.descendants().forEach(d => {
      d.id = uid++; d._children = d.children; d.ti = ti;
      let a = d; while (a.depth > 1) a = a.parent;
      d.branch = a.depth === 0 ? -1 : a.parent.children.indexOf(a);
    });
    root.descendants().forEach(d => { if (d.depth >= 2 && d.children) { d._children = d.children; d.children = null; } });
    const g = canvas.append('g').attr('class', 'tree');
    return { data, root, g, gLink: g.append('g'), gNode: g.append('g'), ti, yOffset: 0 };
  });

  function layout() {
    let offset = 0;
    states.forEach(s => {
      tree(s.root);
      let minX = Infinity, maxX = -Infinity;
      s.root.each(d => { minX = Math.min(minX, d.x); maxX = Math.max(maxX, d.x); });
      s.yOffset = offset - minX;
      offset += (maxX - minX) + GAP;
    });
  }

  function renderTree(s, src) {
    const t = svg.transition().duration(320);
    const nodes = s.root.descendants().reverse();
    const links = s.root.links();

    const node = s.gNode.selectAll('g.node').data(nodes, d => d.id);
    const nodeEnter = node.enter().append('g').attr('class', 'node')
      .attr('transform', 'translate(' + src.y0 + ',' + src.x0 + ')')
      .attr('fill-opacity', 0).attr('stroke-opacity', 0)
      .on('click', (e, d) => {
        const hasKids = d.children || d._children;
        if (e.metaKey || e.ctrlKey || !hasKids) { if (d.data.url) window.open(d.data.url, '_blank'); return; }
        d.children = d.children ? null : d._children; refresh(d);
      });

    nodeEnter.append('rect')
      .attr('height', d => d.depth === 0 ? 32 : 28).attr('y', d => d.depth === 0 ? -16 : -14)
      .attr('rx', 13).attr('ry', 13)
      .attr('x', d => -nodeW(d.data.name, d.depth === 0) / 2)
      .attr('width', d => nodeW(d.data.name, d.depth === 0))
      .attr('fill', 'var(--card)')
      .attr('stroke', colorOf).attr('stroke-width', d => d.depth === 0 ? 2.4 : 1.6);

    nodeEnter.append('circle').attr('r', 4)
      .attr('cx', d => -nodeW(d.data.name, d.depth === 0) / 2).attr('fill', colorOf);

    nodeEnter.append('text').attr('class', d => 'label' + (d.depth === 0 ? ' root' : ''))
      .attr('text-anchor', 'middle').attr('dy', '0.32em')
      .text(d => fitText(d.data.name, d.depth === 0))
      .append('title').text(d => d.data.name);

    const badge = nodeEnter.append('g').attr('class', 'badge')
      .attr('transform', d => 'translate(' + (nodeW(d.data.name, d.depth === 0) / 2 - 2) + ',0)');
    badge.append('circle').attr('r', 9).attr('fill', colorOf);
    badge.append('text').attr('class', 'count').attr('text-anchor', 'middle').attr('dy', '0.32em');

    const merged = node.merge(nodeEnter);
    merged.select('.badge').style('display', d => collapsed(d) ? null : 'none');
    merged.select('.badge .count').text(d => collapsed(d) ? d._children.length : '');
    merged.transition(t)
      .attr('transform', d => 'translate(' + d.y + ',' + d.x + ')')
      .attr('fill-opacity', 1).attr('stroke-opacity', 1);

    node.exit().transition(t).remove()
      .attr('transform', 'translate(' + src.y + ',' + src.x + ')')
      .attr('fill-opacity', 0).attr('stroke-opacity', 0);

    const link = s.gLink.selectAll('path').data(links, d => d.target.id);
    const linkEnter = link.enter().append('path').attr('class', 'link')
      .attr('stroke', d => colorOf(d.target))
      .attr('d', () => { const o = { x: src.x0, y: src.y0 }; return diagonal({ source: o, target: o }); });
    link.merge(linkEnter).transition(t).attr('d', diagonal).attr('stroke', d => colorOf(d.target));
    link.exit().transition(t).remove()
      .attr('d', () => { const o = { x: src.x, y: src.y }; return diagonal({ source: o, target: o }); });

    s.root.eachBefore(d => { d.x0 = d.x; d.y0 = d.y; });
  }

  function refresh(source) {
    layout();
    states.forEach(s => {
      const src = (source && source.ti === s.ti) ? source : s.root;
      s.g.transition().duration(320).attr('transform', 'translate(' + MARGIN_L + ',' + s.yOffset + ')');
      renderTree(s, src);
    });
  }

  function fit() {
    layout();
    let minX = Infinity, maxX = -Infinity, minY = Infinity, maxY = -Infinity;
    states.forEach(s => s.root.each(d => {
      const X = MARGIN_L + d.y, Y = s.yOffset + d.x;
      minX = Math.min(minX, X); maxX = Math.max(maxX, X);
      minY = Math.min(minY, Y); maxY = Math.max(maxY, Y);
    }));
    const w = window.innerWidth, h = window.innerHeight;
    const tw = (maxX - minX) + 380, th = (maxY - minY) + 160;
    const k = Math.min(1.4, Math.min(w / tw, h / th));
    const cx = (minX + maxX) / 2, cy = (minY + maxY) / 2;
    svg.transition().duration(440).call(zoom.transform,
      d3.zoomIdentity.translate(w / 2 - k * cx, h / 2 - k * cy).scale(k));
  }

  d3.select('#expand').on('click', () => { states.forEach(s => s.root.each(d => { if (d._children) d.children = d._children; })); refresh(null); setTimeout(fit, 360); });
  d3.select('#collapse').on('click', () => { states.forEach(s => s.root.descendants().forEach(d => { if (d.depth >= 1 && d.children) { d._children = d.children; d.children = null; } })); refresh(null); setTimeout(fit, 360); });
  d3.select('#fit').on('click', fit);
  window.addEventListener('resize', fit);

  refresh(null);
  setTimeout(fit, 60);
})();
